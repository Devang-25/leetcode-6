class Solution {
    
    // 1 2 3 4 5 6 .. 10 11 .. 15 .. 20 .. 25 .. 30
    // 1 2 6 24 120 720 .. 3628800 39916800 .. 1307674368000 .. 2432902008176640000 .. 15511210043330985984000000 .. 265252859812191058636308480000000

    // 5 - 1 (1)
    // 6 - 1 (1)
    // 7 - 1 (1)
    // 10 - 2 (2)
    // 11 - 2 (2)
    // 15 - 3 (3)
    // 20 - 4 (4)
    // 25 - 6 (5)
    // 50 - 12 (10)
    // 75 - 18 (15)
    // 100 - 24 (20)
    // 125 - 31 (25) Not 30!!!

    // f(124) = n/5 + n/25 = 30
    // f(125) = n/5 + n/25 + n/125 = 31
    // f(625) = n/5 + n/25 + n/125 + n/625
    // f(n) = n/5 + n/25 + n/125 + n/625 + ... + n/5^x
    public int trailingZeroes(int number) {
        int result = 0;

        while (number > 4) {
            number /= 5;
            result += number;
        }
        
        return result;
    }
}